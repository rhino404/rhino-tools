#!/usr/bin/env node
/**
 * Generates src/datasets/index.json — the dataset catalog ("the database
 * index"). Deterministic: contains no wall-clock timestamp, so re-running only
 * changes the file when dataset content changes. This lets CI verify the
 * committed manifest is up to date via `git diff --exit-code`.
 *
 * Category display metadata (labels, icons, subcategory labels) lives in
 * data-pipeline/category-meta.json — edit that file, not quizMeta.js.
 * This script also auto-generates src/data/quizMeta.js as a static fallback.
 *
 * Usage: node data-pipeline/generate-manifest.mjs
 */
import fs from 'node:fs';
import path from 'node:path';
import crypto from 'node:crypto';
import { fileURLToPath, pathToFileURL } from 'node:url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const SRC = path.join(ROOT, 'src');
const OUT = path.join(SRC, 'datasets', 'index.json');
const QUIZ_META_OUT = path.join(SRC, 'data', 'quizMeta.js');

const { DATA_SOURCES } = await import(pathToFileURL(path.join(SRC, 'core', 'dataSources.js')).href);
const categoryMeta = JSON.parse(fs.readFileSync(path.join(__dirname, 'category-meta.json'), 'utf8'));
const examDefs = JSON.parse(fs.readFileSync(path.join(SRC, 'datasets', 'exam.json'), 'utf8'));
const poolsPath = path.join(__dirname, 'pools.json');
const pools = fs.existsSync(poolsPath) ? JSON.parse(fs.readFileSync(poolsPath, 'utf8')).pools : {};
const EMPTY_POOL = { version: null, effectiveDate: null, expiryDate: null, source: null, sourceUrl: null };

const labelFor = (cat, sub) => categoryMeta[cat]?.subcategories?.[sub] || sub;
const examFor = (cat, sub) => {
  const def = examDefs.find((e) => e.category === cat)?.subcategory?.[sub];
  return def ? { totalQuestions: def.totalQuestions, passingScore: def.passingScore } : null;
};

const datasets = [];
let totalQuestions = 0;

for (const [category, subs] of Object.entries(DATA_SOURCES)) {
  for (const [subcategory, ref] of Object.entries(subs)) {
    const rel = ref.replace(/^\.\//, '');
    const abs = path.join(SRC, rel);
    const raw = fs.readFileSync(abs, 'utf8');
    const data = JSON.parse(raw);
    const topics = [...new Set(data.map((q) => q.topic_label).filter(Boolean))].sort();
    const tags = [...new Set(data.flatMap((q) => q.tags || []))].sort();
    datasets.push({
      category,
      subcategory,
      label: labelFor(category, subcategory),
      path: rel,
      count: data.length,
      topics,
      tags,
      sha256: crypto.createHash('sha256').update(raw).digest('hex'),
      exam: examFor(category, subcategory),
      pool: pools[`${category}/${subcategory}`] || EMPTY_POOL,
    });
    totalQuestions += data.length;
  }
}

// Top-level categories array — ordered by DATA_SOURCES key order
const catsSeen = new Set();
const categories = [];
for (const cat of Object.keys(DATA_SOURCES)) {
  if (!catsSeen.has(cat)) {
    catsSeen.add(cat);
    categories.push({
      value: cat,
      label: categoryMeta[cat]?.label || cat,
      icon: categoryMeta[cat]?.icon || '',
    });
  }
}

const manifest = {
  description: 'Ryno Tools dataset catalog. Generated by data-pipeline/generate-manifest.mjs — do not edit by hand.',
  totalQuestions,
  datasetCount: datasets.length,
  categories,
  datasets,
};

fs.writeFileSync(OUT, JSON.stringify(manifest, null, 2) + '\n');
console.log(`Wrote ${path.relative(ROOT, OUT)} — ${datasets.length} datasets, ${totalQuestions} questions`);

// Auto-generate src/data/quizMeta.js as a static first-paint fallback.
// Source of truth is category-meta.json — do not edit quizMeta.js by hand.
const subcategoriesArr = datasets.map(ds => ({ label: ds.label, value: ds.subcategory, category: ds.category }));
const iconEntries = categories.map(c => `    "${c.value}": "${c.icon}"`).join(',\n');

const quizMetaSource = `// AUTO-GENERATED by data-pipeline/generate-manifest.mjs — do not edit by hand.
// To change labels or icons: edit data-pipeline/category-meta.json
// To add/remove datasets: edit src/core/dataSources.js
// Then run: node data-pipeline/generate-manifest.mjs

export const categories = ${JSON.stringify(categories.map(c => ({ label: c.label, value: c.value })), null, 4)};

export const subcategories = ${JSON.stringify(subcategoriesArr, null, 4)};

export const quizMeta = {
    categories,
    subcategories,
};

export const getCategoryIcon = {
    "all": "🌐",
${iconEntries},
};
`;

fs.writeFileSync(QUIZ_META_OUT, quizMetaSource, 'utf8');
console.log(`Wrote ${path.relative(ROOT, QUIZ_META_OUT)} — ${categories.length} categories`);
